// Funções Declarativas

function FuncaoDeclarativa(){
    console.log('Esta mensagem vem de uma função declarativa.');
}

FuncaoDeclarativa();

// Expressão de função
// Com nomeação/ Sem nomeação

var Expressao = function Funcao(){
    console.log("Mensagem de uma expressão de função.");
}

Expressao();

// Arrow Funções - Funções anônimas

var Arrow = () => {
    console.log("Mensagem de uma Arrow Function.");
}

Arrow();

