// -----------------------------------------------------
// Tipagem dinâmica
// Os tipos se alteram dinâmicamente

let um = "um";

console.log(typeof(um)); // Exibe tipo String

um = 1;

console.log(typeof(um)); // Exibe tipo Number

um = true;

console.log(typeof(um)); // Exibe tipo Boolean

var dois;

console.log(typeof(dois)); // Exibe tipo Undefined

